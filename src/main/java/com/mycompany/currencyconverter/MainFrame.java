    static String getMovement(double difference){
        String movement;
        
        if(difference == 0){
            return movement = "NO MOVEMENT";
        } else if (difference > 0){
            return movement = "UPWARD";
        }
        else {
            return movement = "DOWNWARD";
        }
    }

