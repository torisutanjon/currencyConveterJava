    static double getUpdatedRate(String currency){
        
        try {
           double rate;
           
            URL apiUrl = new URL("https://free.currconv.com/api/v7/convert?q=" + currency + "_PHP&compact=ultra&apiKey=5ffb75618801e0556531");
            HttpURLConnection conn = (HttpURLConnection) apiUrl.openConnection();
            conn.setRequestMethod("GET");
            conn.connect();
            
            //check if response code is 200
            int responseCode = conn.getResponseCode();
            if(responseCode != 200){
                throw new RuntimeException("HttpResponseCode: "+ responseCode);
            } else {
                StringBuilder infoString = new StringBuilder();
                Scanner scanner = new Scanner(apiUrl.openStream());
                
                while(scanner.hasNext()){
                    infoString.append(scanner.nextLine());
                }
                scanner.close();
                
                System.out.println(infoString);
                
                //setup JSON-Simple
                JSONParser parser = new JSONParser();
                JSONObject jsonobject = (JSONObject) parser.parse(String.valueOf(infoString));
                
                rate = (double) jsonobject.get(currency + "_PHP");
                return rate;
            }
        } catch (Exception e) {
            System.out.println(e);
            return 0;
        }
    }
    
    static double getPreviousRate(String currency){
    
        try {
            double rate;
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd");
            LocalDate currentDate = LocalDate.now().minusDays(1);
            
            URL apiUrl = new URL("https://free.currconv.com/api/v7/convert?q="+currency+"_PHP&compact=ultra&date="+dtf.format(currentDate)+"&apiKey=5ffb75618801e0556531");
            HttpURLConnection conn = (HttpURLConnection) apiUrl.openConnection();
            conn.setRequestMethod("GET");
            conn.connect();
            
//            System.out.println(apiUrl);
            
            int responseCode = conn.getResponseCode();
            if(responseCode != 200){
                throw new RuntimeException("HTTPResponseCode" + responseCode);
            } else {
                StringBuilder infoString = new StringBuilder();
                Scanner scanner =  new Scanner(apiUrl.openStream());
                
                while(scanner.hasNext()){
                    infoString.append(scanner.nextLine());
                }
                
                scanner.close();    
                
                System.out.println(infoString);
                
                JSONParser parser = new JSONParser();
                JSONObject firstBbject = (JSONObject) parser.parse(String.valueOf(infoString));
                JSONObject secondObject = (JSONObject) parser.parse(String.valueOf(firstBbject.get(currency + "_PHP")));
               
                rate = (double) secondObject.get(dtf.format(currentDate));
                return rate;

            }
        } catch (Exception e) {
            System.out.println("Error in getting Updated rates:" + e);
            return 0;
        } 
    }
    
